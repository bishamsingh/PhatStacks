/* DROP TABLE USER; */
CREATE TABLE USER(
  ID BIGINT AUTO_INCREMENT PRIMARY KEY,
  FIRST_NAME VARCHAR(64),
  LAST_NAME VARCHAR(64),
  EMAIL_ADDRESS VARCHAR(64),
  STREET VARCHAR(64),
  TOWN VARCHAR(64),
  STATE VARCHAR(12),
  COUNTRY VARCHAR(32),
  PHONE_NUMBER VARCHAR(24)
);
